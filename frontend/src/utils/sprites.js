/**
 * Pokemon sprite URLs - local cache first, CDN fallback.
 */
export function getAnimatedSprite(speciesId) {
  return `/sprites/${speciesId}.gif`
}

export function getStaticSprite(speciesId) {
  return `/sprites/${speciesId}.gif`
}

export function getIconSprite(speciesId) {
  return `/sprites/${speciesId}.gif`
}

export function getShowdownSprite(speciesId) {
  return `/sprites/${speciesId}.gif`
}
